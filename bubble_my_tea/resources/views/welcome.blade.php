<section class="bg-pink columns-4">
    <p class="text-center mt-2">Le Bubble Tea c'est quoi ?</p><br> Le thé aux perles, aussi appelé zenzou (chinois : 珍珠奶茶 ; pinyin : zhēnzhū nǎichá ; litt. « thé au lait de perle »), boba (translittération de bubble en chinois), bubble tea ou thé aux bulles par calque de l'anglais1, est une boisson originaire de Taïwan. C'est un mélange de thé froid ou chaud et de lait, parfumé de diverses saveurs et additionné de boules noires de tapioca, qui donnent son nom au thé aux perles. Ces dernières s'aspirent généralement au moyen d'une paille de gros diamètre.<p>
        
       
        <p class="text-center mt-2">Comment le prépare t-on ?</p><br><p>Le thé aux perles a, dans les restaurants et les cafés, l'aspect d'un jus ou d'un milk-shake. On y fait infuser le thé dans du lait ou de l'eau en y ajoutant de la poudre parfumée, notamment aux fruits.

            Pour préparer cette boisson, on achète des billes à thé aux perles (perles de tapioca) dans la plupart des épiceries asiatiques. Afin qu'elles soient propres à la consommation, le cuisinier porte à ébullition les billes de tapioca, qui ont l'aspect de gros bleuets gélatineux après avoir cuit pendant vingt-cinq minutes. Par la suite, les billes sont rincées, essorées, et légèrement recouvertes de sucre brun ou de sirop.
            
            Le thé dans lequel les billes sont plongées est réalisé à partir de poudre à thé aux perles. Il est d'usage de mettre une cuillère à thé et demie de poudre pour deux tasses et demie d'eau ou de lait. Ce thé est produit par le mélange du liquide et de la poudre.
            
            Pour servir un thé aux perles, on place les billes dans un verre au fond suffisamment large, pour ne pas les entasser et en faciliter l'aspiration avec la paille, juste assez large pour laisser passer les billes.<p>
           
